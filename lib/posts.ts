export function getPosts(): Post[] {
  if (typeof window === "undefined") return defaultPosts

  const stored = localStorage.getItem("blog_posts")
  if (stored) {
    try {
      return JSON.parse(stored)
    } catch {
      return defaultPosts
    }
  }
  return defaultPosts
}

export function getPublishedPosts(): Post[] {
  return getPosts().filter((post) => post.published !== false)
}

export function savePosts(posts: Post[]): void {
  if (typeof window !== "undefined") {
    localStorage.setItem("blog_posts", JSON.stringify(posts))
  }
}

export const posts = defaultPosts

export const categorySlugToName: Record<string, string> = {
  gospel: "Сайн мэдээ",
  advent: "Advent",
  health: "Ухал ба амилал",
  education: "Сургаалт зүйрлэлүүд",
  games: "Мөнх үүний өйлгөлт",
  youth: "Тамын тухай",
}

export function getPostBySlug(slug: string): Post | undefined {
  const allPosts = getPosts()
  return allPosts.find((post) => post.slug === slug)
}

export function getPostsByCategory(categorySlugOrName: string): Post[] {
  const allPosts = getPosts()
  if (categorySlugOrName === "Бүгд") return allPosts.filter((post) => post.published !== false)

  const categoryName = categorySlugToName[categorySlugOrName] || categorySlugOrName

  return allPosts.filter((post) => post.category === categoryName && post.published !== false)
}

